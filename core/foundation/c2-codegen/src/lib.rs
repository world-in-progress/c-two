fn render_codec_requirements(
    output: &mut String,
    const_prefix: &str,
    requirements: &BTreeMap<String, Value>,
) -> Result<(), CodegenError> {
    output.push_str(&format!(
        "export const {const_prefix}_CODEC_REQUIREMENTS: readonly C2CodecRequirement[] = "
    ));
    if requirements.is_empty() {
        output.push_str("[];\n\n");
        return Ok(());
    }
    output.push_str("[\n");
    for codec in requirements.values() {
        output.push_str("  {\n");
        output.push_str(&format!("    id: {},\n", ts_string(codec_id(codec)?)));
        output.push_str(&format!(
            "    version: {},\n",
            ts_string(optional_string(codec, "version").unwrap_or(""))
        ));
        if let Some(schema) = optional_string(codec, "schema") {
            output.push_str(&format!("    schema: {},\n", ts_string(schema)));
        }
        if let Some(schema_sha256) = optional_string(codec, "schema_sha256") {
            output.push_str(&format!(
                "    schemaSha256: {},\n",
                ts_string(schema_sha256)
            ));
        }
        if let Some(media_type) = optional_string(codec, "media_type") {
            output.push_str(&format!("    mediaType: {},\n", ts_string(media_type)));
        }
        output.push_str("    capabilities: [");
        let capabilities = codec
            .get("capabilities")
            .and_then(Value::as_array)
            .map(Vec::as_slice)
            .unwrap_or(&[]);
        for (index, item) in capabilities.iter().enumerate() {
            if index > 0 {
                output.push_str(", ");
            }
            output.push_str(&ts_string(item.as_str().unwrap_or("")));
        }
        output.push_str("],\n  },\n");
    }
    output.push_str("];\n\n");
    Ok(())
}

fn render_method_wire_specs(
    output: &mut String,
    const_prefix: &str,
    methods: &[Value],
) -> Result<(), CodegenError> {
    output.push_str(&format!(
        "export const {const_prefix}_METHOD_WIRE: Readonly<Record<string, C2MethodWireSpec>> = {{\n"
    ));
    for method in methods {
        let method_obj = object_at(method, "$.methods[]")?;
        let method_name = string_at(
            required(method_obj, "$.methods[]", "name")?,
            "$.methods[].name",
        )?;
        let wire = object_at(
            required(method_obj, "$.methods[]", "wire")?,
            "$.methods[].wire",
        )?;
        output.push_str(&format!("  {}: {{\n", ts_string(method_name)));
        for field in ["input", "output"] {
            let Some(codec) = wire.get(field) else {
                continue;
            };
            if codec.is_null() {
                continue;
            }
            output.push_str(&format!(
                "    {field}: {},\n",
                codec_requirement_literal(codec)?
            ));
        }
        output.push_str("  },\n");
    }
    output.push_str("};\n\n");
    Ok(())
}

fn codec_requirement_literal(codec: &Value) -> Result<String, CodegenError> {
    let mut parts = Vec::new();
    parts.push(format!("id: {}", ts_string(codec_id(codec)?)));
    parts.push(format!(
        "version: {}",
        ts_string(optional_string(codec, "version").unwrap_or(""))
    ));
    if let Some(schema) = optional_string(codec, "schema") {
        parts.push(format!("schema: {}", ts_string(schema)));
    }
    if let Some(schema_sha256) = optional_string(codec, "schema_sha256") {
        parts.push(format!("schemaSha256: {}", ts_string(schema_sha256)));
    }
    if let Some(media_type) = optional_string(codec, "media_type") {
        parts.push(format!("mediaType: {}", ts_string(media_type)));
    }
    let capabilities = codec
        .get("capabilities")
        .and_then(Value::as_array)
        .map(Vec::as_slice)
        .unwrap_or(&[]);
    let rendered_capabilities = capabilities
        .iter()
        .map(|item| ts_string(item.as_str().unwrap_or("")))
        .collect::<Vec<_>>()
        .join(", ");
    parts.push(format!("capabilities: [{rendered_capabilities}]"));
    Ok(format!("{{ {} }}", parts.join(", ")))
}

