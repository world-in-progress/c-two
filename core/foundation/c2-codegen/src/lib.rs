fn pascal_identifier(value: &str) -> String {
    let ident = identifier(value);
    let mut chars = ident.chars();
    match chars.next() {
        Some(first) => first.to_uppercase().chain(chars).collect(),
        None => "Contract".to_string(),
    }
}

fn const_identifier(value: &str) -> String {
    let mut result = String::new();
    let mut previous_lower_or_digit = false;
    for ch in value.chars() {
        if ch.is_ascii_alphanumeric() {
            if ch.is_ascii_uppercase() && previous_lower_or_digit && !result.ends_with('_') {
                result.push('_');
            }
            result.push(ch.to_ascii_uppercase());
            previous_lower_or_digit = ch.is_ascii_lowercase() || ch.is_ascii_digit();
        } else if !result.ends_with('_') {
            result.push('_');
            previous_lower_or_digit = false;
        }
    }
    let result = result.trim_matches('_').to_string();
    if result.is_empty() {
        "CONTRACT".to_string()
    } else if result.chars().next().is_some_and(|ch| ch.is_ascii_digit()) {
        format!("_{result}")
    } else {
        result
    }
}

fn member_identifier(value: &str) -> String {
    identifier(value)
}

fn identifier(value: &str) -> String {
    let mut result = String::new();
    for (index, ch) in value.chars().enumerate() {
        let valid = ch == '_' || ch == '$' || ch.is_ascii_alphanumeric();
        if !valid {
            result.push('_');
            continue;
        }
        if index == 0 && ch.is_ascii_digit() {
            result.push('_');
        }
        result.push(ch);
    }
    if result.is_empty() {
        result.push('_');
    }
    if is_ts_reserved(&result) {
        result.push('_');
    }
    result
}

