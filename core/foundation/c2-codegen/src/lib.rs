struct RenderedParameters {
    signature: String,
    arg_identifiers: Vec<String>,
}

fn render_parameters(
    params: &[Value],
    input_wire_codec: Option<&Value>,
) -> Result<RenderedParameters, CodegenError> {
    let mut identifiers = BTreeMap::new();
    let mut signature = Vec::new();
    let mut arg_identifiers = Vec::new();
    for param in params {
        let param_obj = object_at(param, "$.methods[].parameters[]")?;
        let original_name = string_at(
            required(param_obj, "$.methods[].parameters[]", "name")?,
            "$.methods[].parameters[].name",
        )?;
        let name = identifier(original_name);
        if let Some(previous) = identifiers.insert(name.clone(), original_name) {
            return Err(CodegenError::InvalidDescriptor {
                path: "$.methods[].parameters".to_string(),
                message: format!(
                    "parameter name collision after TypeScript identifier sanitization: {previous:?} and {original_name:?} both map to {name:?}"
                ),
            });
        }
        let default = object_at(
            required(param_obj, "$.methods[].parameters[]", "default")?,
            "$.methods[].parameters[].default",
        )?;
        let optional = string_at(
            required(default, "$.methods[].parameters[].default", "kind")?,
            "$.methods[].parameters[].default.kind",
        )? != "missing";
        let type_name = annotation_type_for_wire(
            required(param_obj, "$.methods[].parameters[]", "type")?,
            input_wire_codec,
        )?;
        signature.push(format!(
            "{name}{}: {type_name}",
            if optional { "?" } else { "" }
        ));
        arg_identifiers.push(name);
    }
    Ok(RenderedParameters {
        signature: signature.join(", "),
        arg_identifiers,
    })
}

