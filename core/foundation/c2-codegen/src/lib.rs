//! SDK code generation for validated C-Two contract descriptors.

use serde_json::{Map, Value};
use std::collections::BTreeMap;
use thiserror::Error;

const FASTDB_METHOD_CODEC_IDS: &[&str] = &["org.fastdb.call-db"];
const FASTDB_CALL_DB_SCHEMA: &str = "fastdb.call-db.schema.v1";
const GENERATED_HEADER: &str = "/* Generated by c3 contract codegen typescript. */";

#[derive(Debug, Clone, Default)]
pub struct TypeScriptOptions {
    pub strict_codecs: bool,
}

#[derive(Debug, Error)]
pub enum CodegenError {
    #[error(transparent)]
    Contract(#[from] c2_contract::ContractError),
    #[error("contract descriptor must be valid JSON: {0}")]
    InvalidJson(String),
    #[error("contract descriptor invalid at {path}: {message}")]
    InvalidDescriptor { path: String, message: String },
    #[error(
        "unsupported codec {id}; provide a TypeScript codec provider or rerun without strict codec mode to emit opaque requirements"
    )]
    UnsupportedCodec { id: String },
}

pub fn generate_typescript_client(
    descriptor_json: &[u8],
    options: TypeScriptOptions,
) -> Result<String, CodegenError> {
    c2_contract::validate_portable_contract_descriptor_json(descriptor_json)?;
    let value: Value = serde_json::from_slice(descriptor_json)
        .map_err(|err| CodegenError::InvalidJson(err.to_string()))?;
    let digest = c2_contract::contract_descriptor_sha256_hex(descriptor_json)?;
    let root = object_at(&value, "$")?;
    let crm = object_at(required(root, "$", "crm")?, "$.crm")?;
    let crm_name = string_at(required(crm, "$.crm", "name")?, "$.crm.name")?;
    let methods = array_at(required(root, "$", "methods")?, "$.methods")?;
    if options.strict_codecs {
        validate_strict_codecs(methods)?;
    }
    let requirements = collect_codec_requirements(methods)?;

    let const_prefix = const_identifier(crm_name);
    let class_name = format!("{}Client", pascal_identifier(crm_name));
    let mut output = String::new();
    output.push_str(GENERATED_HEADER);
    output.push_str("\n\n");
    render_support_types(&mut output);
    render_contract_const(&mut output, &const_prefix, root, crm, &digest)?;
    render_codec_requirements(&mut output, &const_prefix, &requirements)?;
    render_method_wire_specs(&mut output, &const_prefix, methods)?;
    render_codec_stubs(&mut output, &const_prefix, &requirements);
    render_client_class(&mut output, &class_name, &const_prefix, methods)?;
    render_codec_transport_factory(&mut output, &class_name, &const_prefix);
    render_helpers(&mut output);
    Ok(output)
}

