fn render_client_class(
    output: &mut String,
    class_name: &str,
    const_prefix: &str,
    methods: &[Value],
) -> Result<(), CodegenError> {
    output.push_str(&format!(
        "export class {class_name} {{\n  constructor(private readonly transport: C2ClientTransport, private readonly routeName: string) {{}}\n\n"
    ));
    let mut method_identifiers = BTreeMap::new();
    for method in methods {
        let method_obj = object_at(method, "$.methods[]")?;
        let method_name = string_at(
            required(method_obj, "$.methods[]", "name")?,
            "$.methods[].name",
        )?;
        let method_ident = member_identifier(method_name);
        if let Some(previous) = method_identifiers.insert(method_ident.clone(), method_name) {
            return Err(CodegenError::InvalidDescriptor {
                path: "$.methods".to_string(),
                message: format!(
                    "method name collision after TypeScript identifier sanitization: {previous:?} and {method_name:?} both map to {method_ident:?}"
                ),
            });
        }
        let hold_method_ident = member_identifier(&format!("hold_{method_ident}"));
        if let Some(previous) = method_identifiers.insert(hold_method_ident.clone(), method_name) {
            return Err(CodegenError::InvalidDescriptor {
                path: "$.methods".to_string(),
                message: format!(
                    "method name collision after TypeScript held-method generation: {previous:?} already maps to {hold_method_ident:?} required by {method_name:?}"
                ),
            });
        }
        let params = array_at(
            required(method_obj, "$.methods[]", "parameters")?,
            "$.methods[].parameters",
        )?;
        let wire = object_at(
            required(method_obj, "$.methods[]", "wire")?,
            "$.methods[].wire",
        )?;
        let input_wire_codec = wire.get("input").filter(|value| !value.is_null());
        let output_wire_codec = wire.get("output").filter(|value| !value.is_null());
        let return_type = return_type(
            required(method_obj, "$.methods[]", "return")?,
            output_wire_codec,
        )?;
        let rendered_params = render_parameters(params, input_wire_codec)?;
        let arg_list = rendered_params.arg_identifiers.join(", ");
        let args_expr = if arg_list.is_empty() {
            "[]".to_string()
        } else {
            format!("compactTrailingUndefined([{arg_list}])")
        };
        output.push_str(&format!(
            "  async {method_ident}({}): Promise<{return_type}> {{\n    return this.transport.call<{return_type}>(\n      this.routeName,\n      {const_prefix}_CONTRACT,\n      {},\n      {args_expr},\n    );\n  }}\n\n",
            rendered_params.signature,
            ts_string(method_name),
        ));
        output.push_str(&format!(
            "  async {hold_method_ident}({}): Promise<C2HeldResult<{return_type}>> {{\n    return requireHeldTransport(this.transport).hold<{return_type}>(\n      this.routeName,\n      {const_prefix}_CONTRACT,\n      {},\n      {args_expr},\n    );\n  }}\n\n",
            rendered_params.signature,
            ts_string(method_name),
        ));
    }
    output.push_str("}\n\n");
    Ok(())
}

