fn annotation_type_for_wire(
    annotation: &Value,
    wire_codec: Option<&Value>,
) -> Result<String, CodegenError> {
    if is_fastdb_call_db_aggregate_annotation(annotation, wire_codec)? {
        return Ok("unknown".to_string());
    }
    annotation_type(annotation)
}

fn annotation_type(annotation: &Value) -> Result<String, CodegenError> {
    let object = object_at(annotation, "$.annotation")?;
    let kind = string_at(
        required(object, "$.annotation", "kind")?,
        "$.annotation.kind",
    )?;
    match kind {
        "none" => Ok("null".to_string()),
        "primitive" => primitive_type(string_at(
            required(object, "$.annotation", "name")?,
            "$.annotation.name",
        )?),
        "list" => {
            let item = annotation_type(required(object, "$.annotation", "item")?)?;
            if item.contains(" | ") {
                Ok(format!("Array<{item}>"))
            } else {
                Ok(format!("{item}[]"))
            }
        }
        "dict" => {
            let value = annotation_type(required(object, "$.annotation", "value")?)?;
            Ok(format!("Record<string, {value}>"))
        }
        "tuple" => {
            let items = array_at(
                required(object, "$.annotation", "items")?,
                "$.annotation.items",
            )?
            .iter()
            .map(annotation_type)
            .collect::<Result<Vec<_>, _>>()?;
            Ok(format!("[{}]", items.join(", ")))
        }
        "tuple_variadic" => {
            let item = annotation_type(required(object, "$.annotation", "item")?)?;
            Ok(format!("Array<{item}>"))
        }
        "union" => {
            let items = array_at(
                required(object, "$.annotation", "items")?,
                "$.annotation.items",
            )?
            .iter()
            .map(annotation_type)
            .collect::<Result<Vec<_>, _>>()?;
            Ok(items.join(" | "))
        }
        "codec" => codec_value_type(required(object, "$.annotation", "codec")?),
        "transferable" => codec_value_type(required(object, "$.annotation", "abi_ref")?),
        _ => Ok("unknown".to_string()),
    }
}

fn primitive_type(name: &str) -> Result<String, CodegenError> {
    match name {
        "bool" => Ok("boolean".to_string()),
        "int" | "float" => Ok("number".to_string()),
        "str" => Ok("string".to_string()),
        "bytes" | "memoryview" | "bytearray" => Ok("Uint8Array".to_string()),
        other => Err(CodegenError::InvalidDescriptor {
            path: "$.annotation.name".to_string(),
            message: format!("unsupported primitive {other:?}"),
        }),
    }
}

fn codec_value_type(codec: &Value) -> Result<String, CodegenError> {
    Ok(format!("C2CodecValue<{}>", ts_string(codec_id(codec)?)))
}

fn is_fastdb_call_db_aggregate_annotation(
    annotation: &Value,
    wire_codec: Option<&Value>,
) -> Result<bool, CodegenError> {
    let Some(wire_codec) = wire_codec else {
        return Ok(false);
    };
    if codec_id(wire_codec)? != "org.fastdb.call-db" {
        return Ok(false);
    }
    let object = object_at(annotation, "$.annotation")?;
    let kind = string_at(
        required(object, "$.annotation", "kind")?,
        "$.annotation.kind",
    )?;
    let codec = match kind {
        "codec" => required(object, "$.annotation", "codec")?,
        "transferable" => required(object, "$.annotation", "abi_ref")?,
        _ => return Ok(false),
    };
    Ok(codec_id(codec)? == "org.fastdb.call-db" && codec_key(codec)? == codec_key(wire_codec)?)
}

fn collect_codec_requirements(methods: &[Value]) -> Result<BTreeMap<String, Value>, CodegenError> {
    let mut requirements = BTreeMap::new();
    for method in methods {
        let method_obj = object_at(method, "$.methods[]")?;
        let wire = object_at(
            required(method_obj, "$.methods[]", "wire")?,
            "$.methods[].wire",
        )?;
        for field in ["input", "output"] {
            let Some(codec) = wire.get(field) else {
                continue;
            };
            if codec.is_null() {
                continue;
            }
            let key = codec_key(codec)?;
            requirements.insert(key, codec.clone());
        }
    }
    Ok(requirements)
}

fn validate_strict_codecs(methods: &[Value]) -> Result<(), CodegenError> {
    for method in methods {
        let method_obj = object_at(method, "$.methods[]")?;
        let wire = object_at(
            required(method_obj, "$.methods[]", "wire")?,
            "$.methods[].wire",
        )?;
        for field in ["input", "output"] {
            let Some(codec) = wire.get(field) else {
                continue;
            };
            if codec.is_null() {
                continue;
            }
            let id = codec_id(codec)?;
            if !FASTDB_METHOD_CODEC_IDS.contains(&id) {
                return Err(CodegenError::UnsupportedCodec { id: id.to_string() });
            }
            validate_fastdb_call_db_codec_ref(codec, &format!("$.methods[].wire.{field}"))?;
        }
    }
    Ok(())
}

fn validate_fastdb_call_db_codec_ref(codec: &Value, path: &str) -> Result<(), CodegenError> {
    if optional_string(codec, "schema") != Some(FASTDB_CALL_DB_SCHEMA) {
        return Err(CodegenError::InvalidDescriptor {
            path: format!("{path}.schema"),
            message: format!("fastdb call-db codec refs must use schema {FASTDB_CALL_DB_SCHEMA}"),
        });
    }
    if optional_string(codec, "schema_sha256").is_none() {
        return Err(CodegenError::InvalidDescriptor {
            path: format!("{path}.schema_sha256"),
            message: "fastdb call-db codec refs must include schema_sha256".to_string(),
        });
    }
    let capabilities = codec
        .get("capabilities")
        .and_then(Value::as_array)
        .map(Vec::as_slice)
        .unwrap_or(&[]);
    let mut has_bytes = false;
    for (index, capability) in capabilities.iter().enumerate() {
        let value = capability
            .as_str()
            .ok_or_else(|| CodegenError::InvalidDescriptor {
                path: format!("{path}.capabilities[{index}]"),
                message: "fastdb call-db codec capabilities must be strings".to_string(),
            })?;
        if value == "bytes" {
            has_bytes = true;
        }
    }
    if !has_bytes {
        return Err(CodegenError::InvalidDescriptor {
            path: format!("{path}.capabilities"),
            message: "fastdb call-db codec refs must include bytes capability".to_string(),
        });
    }
    Ok(())
}

fn codec_key(codec: &Value) -> Result<String, CodegenError> {
    let mut capabilities = codec
        .get("capabilities")
        .and_then(Value::as_array)
        .map(Vec::as_slice)
        .unwrap_or(&[])
        .iter()
        .map(|item| {
            item.as_str()
                .ok_or_else(|| CodegenError::InvalidDescriptor {
                    path: "$.codec_ref.capabilities[]".to_string(),
                    message: "expected string".to_string(),
                })
        })
        .collect::<Result<Vec<_>, _>>()?;
    capabilities.sort_unstable();
    let capability_key = capabilities.join(",");
    Ok([
        codec_id(codec)?,
        optional_string(codec, "version").unwrap_or(""),
        optional_string(codec, "schema").unwrap_or(""),
        optional_string(codec, "schema_sha256").unwrap_or(""),
        optional_string(codec, "media_type").unwrap_or(""),
        &capability_key,
    ]
    .join("|"))
}

fn codec_id(codec: &Value) -> Result<&str, CodegenError> {
    let object = object_at(codec, "$.codec_ref")?;
    string_at(required(object, "$.codec_ref", "id")?, "$.codec_ref.id")
}

